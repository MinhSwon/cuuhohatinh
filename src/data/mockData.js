// Dashboard stats
export const getDashboardStats = (data) => {
  const { rescueRequests, rescueTeams, citizens, warnings } = data;
  return {
    total_citizens: citizens?.length || 0,
    total_vulnerable: VULNERABLE_HOUSEHOLDS.length,
    total_rescue_teams: rescueTeams?.length || 0,
    active_warnings: warnings?.filter(w => w.status === 'PUBLISHED').length || 0,
    total_requests: rescueRequests?.length || 0,
    pending_requests: rescueRequests?.filter(r => r.status === 'PENDING').length || 0,
    processing_requests: rescueRequests?.filter(r => ['ASSIGNED','ACCEPTED','MOVING','NEAR_VICTIM','ARRIVED_CONFIRMED','RESCUING'].includes(r.status)).length || 0,
    rescued_requests: rescueRequests?.filter(r => r.status === 'RESCUED' || r.status === 'TRANSFERRED_SAFEZONE').length || 0,
    unreachable_requests: rescueRequests?.filter(r => r.status === 'UNREACHABLE').length || 0,
    total_sms: SMS_LOGS.length,
  };
};
